import tkinter as tk
import tutor as tut

TITLE_FONT = ("Helvetica", 18, "bold")

class SampleApp(tk.Tk):

    def __init__(self, *args, **kwargs):
        tk.Tk.__init__(self, *args, **kwargs)

        # the container is where we'll stack a bunch of frames
        # on top of each other, then the one we want visible
        # will be raised above the others
        container = tk.Frame(self)
        container.pack(side="top", fill="both", expand=True)
        container.grid_rowconfigure(0, weight=1)
        container.grid_columnconfigure(0, weight=1)

        self.frames = {}
        for F in (StartPage, PageOne, PageTwo, PageThree, PageFour, PageFive):
            page_name = F.__name__
            frame = F(container, self)
            self.frames[page_name] = frame

            # put all of the pages in the same location;
            # the one on the top of the stacking order
            # will be the one that is visible.
            frame.grid(row=0, column=0, sticky="nsew")

        self.show_frame("StartPage")

    def show_frame(self, page_name):
        '''Show a frame for the given page name'''
        frame = self.frames[page_name]
        frame.tkraise()


class StartPage(tk.Frame):

    def __init__(self, parent, controller):
        tk.Frame.__init__(self, parent)
        self.controller = controller
        label = tk.Label(self, text="This is the start page", font=TITLE_FONT)
        label.pack(side="top", fill="x", pady=10)

        button1 = tk.Button(self, text="Upload csv file and assign new students",
                            command=lambda: controller.show_frame("PageOne"))
        button2 = tk.Button(self, text="Search individual student's personal tutor",
                            command=lambda: controller.show_frame("PageTwo"))
        button3 = tk.Button(self, text="Display lists of tutees for a particular personal tutor",
                            command=lambda: controller.show_frame("PageTwo"))
        button4 = tk.Button(self, text="Display information on the quota of tutees",
                            command=lambda: controller.show_frame("PageTwo"))
        button5 = tk.Button(self, text="Delete a student from tutor list and re-assign a single student",
                            command=lambda: controller.show_frame("PageTwo"))
        button1.pack()
        button2.pack()
        button3.pack()
        button4.pack()
        button5.pack()


